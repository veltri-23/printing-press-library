// PATCH(amend-2026-05-19: batch-csv-export) — Helpers for `coin batch --csv`,
// which streams a flat CSV instead of JSONL. Lives in its own file so the
// dotted-path traversal and CSV-cell shaping are reviewable and testable in
// isolation; coin_batch.go only wires the new flag and delegates emission to
// the helpers below.
//
// The CSV shape projects across the unified {cert_no, data, _keep} envelope
// the rest of the CLI emits since PR #683:
//
//   - cert_no              top-level scalar
//   - data.Name            field inside the coin record
//   - data.Year            ditto
//   - _keep.box            input-row passthrough column
//
// Missing fields emit an empty cell, not the literal string "null" — that
// keeps spreadsheet imports honest (a blank cell is missing data; the string
// "null" silently coerces to a value on totals/lookups). Nested objects/arrays
// at a leaf path are JSON-encoded into one cell so downstream tooling can
// re-parse them; scalars (string/number/bool) are formatted as-is.
//
// See AGENTS.md Priority 7.

package cli

import (
	"encoding/json"
	"fmt"
	"strconv"
	"strings"
)

// csvColumnSpec describes a single column path the user asked for via
// --columns. Path is the dotted form as written ("data.Name"); segments is
// the tokenised version used during lookup. Header is what we write in the
// CSV's first row — for now it's just Path verbatim so the user can correlate
// the input flag with the output column.
type csvColumnSpec struct {
	Path     string
	Header   string
	Segments []string
}

// parseColumnSpec splits the comma-separated --columns flag into one spec
// per column. Whitespace around commas/segments is trimmed; empty entries
// (a stray double comma) are skipped rather than rejected so an autogenerated
// column list with trailing commas doesn't error out.
//
// Returns an error only when every column entry was empty, which signals the
// user asked for --columns but supplied no actual paths.
func parseColumnSpec(raw string) ([]csvColumnSpec, error) {
	if strings.TrimSpace(raw) == "" {
		return nil, nil
	}
	out := []csvColumnSpec{}
	for _, p := range strings.Split(raw, ",") {
		p = strings.TrimSpace(p)
		if p == "" {
			continue
		}
		segs := strings.Split(p, ".")
		for i := range segs {
			segs[i] = strings.TrimSpace(segs[i])
		}
		out = append(out, csvColumnSpec{Path: p, Header: p, Segments: segs})
	}
	if len(out) == 0 {
		return nil, fmt.Errorf("--columns has no non-empty paths")
	}
	return out, nil
}

// lookupPath walks a dotted path against the row's outer envelope (which is
// a map[string]any in coin_batch.go — built by rowBase + post-decoded JSON).
// Returns (value, true) when every segment resolves and the leaf is present;
// returns (nil, false) when any segment misses or hits a non-traversable
// type. The bool lets the caller distinguish a present nil ("PriceGuideValue":
// null after the response transform) from a missing field — both render as
// an empty CSV cell, but the distinction matters for testing.
//
// The traversal only walks into map[string]any. Arrays at non-leaf positions
// are treated as a miss; supporting [N] index syntax would expand --columns
// past the AGENTS.md acceptance criteria and is left for a follow-up.
func lookupPath(envelope map[string]any, segments []string) (any, bool) {
	cur := any(envelope)
	for _, seg := range segments {
		m, ok := cur.(map[string]any)
		if !ok {
			return nil, false
		}
		next, present := m[seg]
		if !present {
			return nil, false
		}
		cur = next
	}
	return cur, true
}

// csvCellFromValue shapes a Go value (decoded from JSON) into the CSV cell
// representation. Empty for nil; raw text for strings/numbers/bools;
// JSON-encoded for everything else (maps, arrays). The CSV writer handles
// quoting and escaping; this helper's only job is the value→string mapping.
//
// nil → "" is the contract from AGENTS.md P7: a missing field emits an empty
// cell, NOT the literal string "null".
func csvCellFromValue(v any) string {
	if v == nil {
		return ""
	}
	switch t := v.(type) {
	case string:
		return t
	case bool:
		return strconv.FormatBool(t)
	case float64:
		// JSON's number type. Format as an integer when it has no fractional
		// part so a Year column reads "1881" instead of "1881.000000".
		if t == float64(int64(t)) {
			return strconv.FormatInt(int64(t), 10)
		}
		return strconv.FormatFloat(t, 'f', -1, 64)
	default:
		// Maps, slices, or anything else exotic: JSON-encode so the cell
		// carries the structure and downstream tooling can re-parse.
		b, err := json.Marshal(v)
		if err != nil {
			return fmt.Sprintf("%v", v)
		}
		return string(b)
	}
}

// envelopeToAnyMap normalizes the row envelope into a fully decoded
// map[string]any tree so lookupPath can walk it. The runBatch builder stores
// data/cached as json.RawMessage; auto-header discovery and column lookups
// need plain Go types under the hood.
//
// Walks one level deep: the top-level keys (cert_no, data, _keep, error) are
// re-decoded when their value is a json.RawMessage; everything else passes
// through. Two levels would re-decode the inner coin record too but isn't
// needed — encoding/json hands us map[string]any for inner objects when we
// pass a json.RawMessage through json.Unmarshal into any.
func envelopeToAnyMap(obj map[string]any) map[string]any {
	out := make(map[string]any, len(obj))
	for k, v := range obj {
		if raw, ok := v.(json.RawMessage); ok {
			var decoded any
			if err := json.Unmarshal(raw, &decoded); err == nil {
				out[k] = decoded
				continue
			}
		}
		out[k] = v
	}
	return out
}

// autoColumnsFromEnvelope discovers a stable header set from a row envelope
// when the user did NOT pass --columns. The order is deterministic:
//
//  1. cert_no (always first when present)
//  2. _keep.<key> for every top-level key inside _keep, alphabetically sorted
//  3. data.<key> for every top-level key inside data, alphabetically sorted
//  4. error (always emitted, even when the trigger envelope has no error key)
//
// Map-key sort (see sortedMapKeys) is what makes the auto-header stable across
// runs given Go's randomised map iteration; it does NOT preserve the original
// CSV column order. Users who need a specific column order should pass
// --columns explicitly. This is also intentionally shallow — deep CoinFacts
// nesting (e.g. AuctionList arrays) would explode the column count beyond
// utility.
//
// runBatch derives the schema from the first envelope that contains `data`
// (a success row by definition), so a conditional `error` column would never
// fire in the normal mixed-success/failure case. The `error` column is
// always present so a later failure row in the same batch has somewhere to
// write its error text instead of emitting an all-empty row indistinguishable
// from a missing-data cert. In the rare all-success batch this surfaces as
// a single trailing empty column — small cost for ensuring no error text
// is ever silently dropped.
func autoColumnsFromEnvelope(envelope map[string]any) []csvColumnSpec {
	out := []csvColumnSpec{}
	if _, ok := envelope["cert_no"]; ok {
		out = append(out, csvColumnSpec{Path: "cert_no", Header: "cert_no", Segments: []string{"cert_no"}})
	}
	if keep, ok := envelope["_keep"].(map[string]any); ok {
		keys := sortedMapKeys(keep)
		for _, k := range keys {
			path := "_keep." + k
			out = append(out, csvColumnSpec{Path: path, Header: path, Segments: []string{"_keep", k}})
		}
	}
	if data, ok := envelope["data"].(map[string]any); ok {
		keys := sortedMapKeys(data)
		for _, k := range keys {
			path := "data." + k
			out = append(out, csvColumnSpec{Path: path, Header: path, Segments: []string{"data", k}})
		}
	}
	out = append(out, csvColumnSpec{Path: "error", Header: "error", Segments: []string{"error"}})
	return out
}

// sortedMapKeys returns the keys of a string-keyed map in sorted order.
// Used by autoColumnsFromEnvelope so the auto-header is deterministic across
// runs even though Go's map iteration order is randomised.
func sortedMapKeys(m map[string]any) []string {
	keys := make([]string, 0, len(m))
	for k := range m {
		keys = append(keys, k)
	}
	// Plain sort — column order isn't user-facing identity, just stability.
	for i := 1; i < len(keys); i++ {
		for j := i; j > 0 && keys[j-1] > keys[j]; j-- {
			keys[j-1], keys[j] = keys[j], keys[j-1]
		}
	}
	return keys
}
