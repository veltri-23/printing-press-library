// Package store provides local SQLite persistence for stackadapt-pp-cli.
//
// StackAdapt's Public API is a single GraphQL endpoint with no REST list
// resources, so the generated REST sync/store layer was never emitted. This
// hand-authored package gives the live-query CLI an offline-capable spine:
// `sync` writes the Relay connection nodes (advertisers, campaigns, campaign
// groups, ads, segments) into a flat `resources` table, and `search` / `sql`
// / `--data-source local` read them back without touching the network.
//
// Uses modernc.org/sqlite (pure Go, no cgo) so the binary still
// cross-compiles to a single static artifact.
//
// Durable hand-authored package (no "Generated by CLI Printing Press"
// header); preserved across `generate --force`.
package store

import (
	"context"
	"database/sql"
	"encoding/json"
	"fmt"
	"os"
	"path/filepath"
	"strings"
	"time"

	_ "modernc.org/sqlite"
)

// Store is a thin SQLite wrapper holding one row per synced API object.
type Store struct {
	db   *sql.DB
	path string
}

const schemaSQL = `
CREATE TABLE IF NOT EXISTS resources (
	resource_type TEXT NOT NULL,
	id            TEXT NOT NULL,
	name          TEXT,
	data          TEXT NOT NULL,
	synced_at     TEXT NOT NULL,
	PRIMARY KEY (resource_type, id)
);
CREATE INDEX IF NOT EXISTS idx_resources_type ON resources(resource_type);
CREATE INDEX IF NOT EXISTS idx_resources_name ON resources(resource_type, name);

CREATE TABLE IF NOT EXISTS sync_state (
	resource_type TEXT PRIMARY KEY,
	synced_at     TEXT NOT NULL,
	row_count     INTEGER NOT NULL
);
`

// Open opens (creating if needed) the SQLite store at path using the
// background context.
func Open(path string) (*Store, error) {
	return OpenWithContext(context.Background(), path)
}

// OpenWithContext opens or creates the store, honoring ctx during the
// schema bootstrap.
func OpenWithContext(ctx context.Context, path string) (*Store, error) {
	if path == "" {
		path = DefaultPath()
	}
	if err := os.MkdirAll(filepath.Dir(path), 0o750); err != nil {
		return nil, fmt.Errorf("creating db directory: %w", err)
	}
	// file: URI prefix is required for modernc.org/sqlite to honor the
	// _pragma query parameters; busy_timeout keeps a read concurrent with a
	// writer from failing immediately with SQLITE_BUSY.
	dsn := "file:" + path + "?_pragma=journal_mode(WAL)&_pragma=busy_timeout(5000)&_pragma=foreign_keys(ON)"
	db, err := sql.Open("sqlite", dsn)
	if err != nil {
		return nil, fmt.Errorf("opening database: %w", err)
	}
	db.SetMaxOpenConns(2)
	if _, err := db.ExecContext(ctx, schemaSQL); err != nil {
		if cerr := db.Close(); cerr != nil {
			return nil, fmt.Errorf("creating schema: %w (and closing db: %v)", err, cerr)
		}
		return nil, fmt.Errorf("creating schema: %w", err)
	}
	return &Store{db: db, path: path}, nil
}

// OpenReadOnly opens an existing store in read-only mode. Used by the `sql`
// command so a stray mutating statement is rejected at the driver level.
func OpenReadOnly(path string) (*Store, error) {
	if path == "" {
		path = DefaultPath()
	}
	if _, err := os.Stat(path); err != nil {
		return nil, fmt.Errorf("no local store at %s: run 'stackadapt-pp-cli sync' first", path)
	}
	dsn := "file:" + path + "?mode=ro&_pragma=busy_timeout(5000)"
	db, err := sql.Open("sqlite", dsn)
	if err != nil {
		return nil, fmt.Errorf("opening database (read-only): %w", err)
	}
	db.SetMaxOpenConns(2)
	return &Store{db: db, path: path}, nil
}

// Close releases the underlying connection pool.
func (s *Store) Close() error { return s.db.Close() }

// Path returns the on-disk location of the SQLite file.
func (s *Store) Path() string { return s.path }

// DB exposes the underlying handle for read-only ad-hoc queries (the `sql`
// command). Callers must not Close it.
func (s *Store) DB() *sql.DB { return s.db }

// Upsert inserts or replaces one object. name is the human label used by
// search and table output; data is the raw JSON node as returned by the API.
func (s *Store) Upsert(ctx context.Context, resourceType, id, name string, data json.RawMessage, syncedAt time.Time) error {
	_, err := s.db.ExecContext(ctx, `
		INSERT INTO resources (resource_type, id, name, data, synced_at)
		VALUES (?, ?, ?, ?, ?)
		ON CONFLICT(resource_type, id) DO UPDATE SET
			name = excluded.name,
			data = excluded.data,
			synced_at = excluded.synced_at
	`, resourceType, id, name, string(data), syncedAt.UTC().Format(time.RFC3339))
	if err != nil {
		return fmt.Errorf("upserting %s/%s: %w", resourceType, id, err)
	}
	return nil
}

// SaveSyncState records the last sync time and row count for a resource type.
func (s *Store) SaveSyncState(ctx context.Context, resourceType string, count int, syncedAt time.Time) error {
	_, err := s.db.ExecContext(ctx, `
		INSERT INTO sync_state (resource_type, synced_at, row_count)
		VALUES (?, ?, ?)
		ON CONFLICT(resource_type) DO UPDATE SET
			synced_at = excluded.synced_at,
			row_count = excluded.row_count
	`, resourceType, syncedAt.UTC().Format(time.RFC3339), count)
	if err != nil {
		return fmt.Errorf("saving sync state for %s: %w", resourceType, err)
	}
	return nil
}

// Count returns the number of stored rows for one resource type.
func (s *Store) Count(ctx context.Context, resourceType string) (int, error) {
	var n int
	err := s.db.QueryRowContext(ctx, `SELECT count(*) FROM resources WHERE resource_type = ?`, resourceType).Scan(&n)
	return n, err
}

// Total returns the number of stored rows across all resource types.
func (s *Store) Total(ctx context.Context) (int, error) {
	var n int
	err := s.db.QueryRowContext(ctx, `SELECT count(*) FROM resources`).Scan(&n)
	return n, err
}

// Types returns a resource_type -> row count map for everything synced.
func (s *Store) Types(ctx context.Context) (map[string]int, error) {
	rows, err := s.db.QueryContext(ctx, `SELECT resource_type, count(*) FROM resources GROUP BY resource_type ORDER BY resource_type`)
	if err != nil {
		return nil, err
	}
	defer rows.Close()
	out := map[string]int{}
	for rows.Next() {
		var t string
		var n int
		if err := rows.Scan(&t, &n); err != nil {
			return nil, err
		}
		out[t] = n
	}
	return out, rows.Err()
}

// List returns up to limit stored objects of a resource type as raw JSON.
func (s *Store) List(ctx context.Context, resourceType string, limit int) ([]json.RawMessage, error) {
	if limit <= 0 {
		limit = 100
	}
	rows, err := s.db.QueryContext(ctx,
		`SELECT data FROM resources WHERE resource_type = ? ORDER BY name, id LIMIT ?`,
		resourceType, limit)
	if err != nil {
		return nil, err
	}
	defer rows.Close()
	out := make([]json.RawMessage, 0)
	for rows.Next() {
		var data string
		if err := rows.Scan(&data); err != nil {
			return nil, err
		}
		out = append(out, json.RawMessage(data))
	}
	return out, rows.Err()
}

// Hit is one search result.
type Hit struct {
	ResourceType string          `json:"resource_type"`
	ID           string          `json:"id"`
	Name         string          `json:"name"`
	Data         json.RawMessage `json:"data"`
}

// Search returns rows whose name or raw JSON contains term (case-insensitive
// substring). resourceType is optional; empty searches every type.
func (s *Store) Search(ctx context.Context, term, resourceType string, limit int) ([]Hit, error) {
	if limit <= 0 {
		limit = 50
	}
	like := "%" + strings.ToLower(term) + "%"
	q := `SELECT resource_type, id, COALESCE(name, ''), data FROM resources
		WHERE (lower(COALESCE(name, '')) LIKE ? OR lower(data) LIKE ?)`
	args := []any{like, like}
	if resourceType != "" {
		q += ` AND resource_type = ?`
		args = append(args, resourceType)
	}
	q += ` ORDER BY resource_type, name, id LIMIT ?`
	args = append(args, limit)

	rows, err := s.db.QueryContext(ctx, q, args...)
	if err != nil {
		return nil, err
	}
	defer rows.Close()
	out := make([]Hit, 0)
	for rows.Next() {
		var h Hit
		var data string
		if err := rows.Scan(&h.ResourceType, &h.ID, &h.Name, &data); err != nil {
			return nil, err
		}
		h.Data = json.RawMessage(data)
		out = append(out, h)
	}
	return out, rows.Err()
}

// DefaultPath returns the canonical store location, overridable via the
// STACKADAPT_DB environment variable. Matches the Printing Press convention
// of ~/.local/share/<cli>/data.db.
func DefaultPath() string {
	if v := os.Getenv("STACKADAPT_DB"); v != "" {
		return v
	}
	home, err := os.UserHomeDir()
	if err != nil || home == "" {
		return "data.db"
	}
	return filepath.Join(home, ".local", "share", "stackadapt-pp-cli", "data.db")
}
